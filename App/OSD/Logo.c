//******************************************************************************
// Prototype: 
//  void ShowLogo(void)
// Parameters:
//  None
// Return:
//  None
// Purpose:
//  None
// Notes:
//  None
//******************************************************************************
void ShowLogo(void)
{
#define LOGO_WIDTH       37
#define LOGO_HEIGHT      23
#define MAIN_FRAME_COLOR 0x0002

    USHRT i, l, m;

    EnableDclk();
    Force2Background(0xE0, 0xF0, 0xF8); // Enable free run mode
    SetPostBGColor(0xE0, 0xF0, 0xF8); // Force BG color as 0xe0,0xf0,0xf8
    MTXAPI_SetRGBGain(255, 255, 255);
    MTXAPI_SetContrast(128);
    SetBrightness(1023);
    OSDCloseAll();
    OSDAPI_LoadPalette(&ucLogoPalette);
    OSDAPI_SetHVZoom(0);
    OSDAPI_SetFontSize(OSD_FONT_12X18);
    m = OSDAPI_GetOSDFrameSize(LOGO_WIDTH, LOGO_HEIGHT);
#if ENABLE_OSD_ROTATION == ON
    OSDAPI_Set1BitFontAddr(OSDSRAM_ADDR_CEILING(m));
    OSDAPI_Set2BitsFontAddr(OSDSRAM_ADDR_FLOOR(OSD_ROT_RAM_SIZE - 1854));
#else
    OSDAPI_Set1BitFontAddr(m);
    OSDAPI_Set2BitsFontAddr(OSD_RAM_SIZE - 1854);
#endif
    OSDAPI_LoadFont(OSD_FONT_COMPRESSED, &ucLogoFont1BitTable);
    OSDInitFrame(LOGO_WIDTH, LOGO_HEIGHT, MAIN_FRAME_COLOR);
    OSDAPI_SetPosition(50,50);
    OSDAPI_InitSeqWriteCode(0);
    l = LOGO_WIDTH * LOGO_HEIGHT;
    for (i = 0; i < l; i++) {
        OSDAPI_SeqWriteCode(LogoFontMapIndex[i]);
    }

    PanelPowerOnSequence(TRUE);
    OSDAPI_SetOSDFgBgTran(0);
    OSDTurnOnOSD();
}

//******************************************************************************
// Prototype: 
//  void CloseLogo(void)
// Parameters:
//  None
// Return:
//  None
// Purpose:
//  Close logo and init some user setting
// Notes:
//  None
//******************************************************************************
void CloseLogo(void)
{
    OSDCloseAll();
    PanelPowerOffSequence();
    SetPostBGColor(0, 0, 0);
    UserSetBrightness();
    UserSetContrast();
    UserSetColorTemp();
    OSDInitialize(TRUE);
}
